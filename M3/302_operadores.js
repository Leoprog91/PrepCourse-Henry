// Operadores y precedencia

// Ya conocimos las variables y tipos de datos, 
// ahora abordaremos un tema más familiar: 
// suma, resta, multiplicación y división. 
// ¿Te suenan? A estos símbolos los llamamos 
// operadores y se usan para calcular un valor 
// de dos o más elementos.

// La precedencia de operadores determina el orden 
// en el cual los operadores son evaluados uno 
// respecto del otro. Los operadores con mayor 
// precedencia se convierten en los operandos 
// de los operadores con menor precedencia.

// Cuadro de precedencias

// Te dejamos a mano el cuadro completo de precedencias 
// para que puedas tenerlo en cuenta al momento de codear.
// https://developer.mozilla.org/es/docs/Web/JavaScript/Reference/Operators/Operator_Precedence#tabla

//Suma
console.log(3 + 5);
// 8

//Resta
console.log(14 - 7);
// 7

//Multiplicacion
console.log(8 * 6);
// 48

//Division
console.log(30 / 9);
// 3.3333333333333335

//Resto
console.log(95 % 4);
// 3

//Prueba de precedencia
console.log(3 + 5 * 2 - 8);
// 5